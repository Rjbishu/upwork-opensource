			<h3><?=$title?></h3>
			<p>The workroom is specific to each job you hire a freelancer for. It allows you to manage the job and gives you information concerning the job’s current status.</p>

			<p>There are a few tabs at the top of the workroom:</p>

			<p><u>Overview:</u></p>

			<p>The overview is the primary tab at the top of the workroom. This is your default view and shows you information concerning the specific freelancer you’re viewing. Everything you see here is between you and the freelancer you’re viewing.</p>

			<p><u>Alerts:</u></p>

			<p>This is a list of job-related alerts. And gives you an easy way to reference when major milestones have occurred, such as a pending agreement and when SafePay is funded.</p>

			<p><u>Tasks:</u></p>

			<p>Assign tasks to anyone hired for the job. Anyone hired for the particular job you’re viewing can access this page.</p>

			<p><u>Discussions:</u></p>

			<p>This is a public discussion area. Anyone hired for your job can participate in the conversation. Anything you want to say privately should NOT be posted here and should instead be sent as a private message.</p>

			<p><u>Files:</u></p>

			<p>This is a public files area. Anyone hired for your job can upload files here. Any files you want to share privately should NOT be uploaded here and should instead be attached in a private message.</p>

			<p>Once you click back on the Overview tab, you’ll see some more tabs that pertain to each specific freelancer you’ve hired. You’ll see the freelancer’s name and profile picture in the overview to indicate which freelancer you are interacting with.</p>

			<p><u>Understanding the main sections of the overview:</u></p>

			<p><u>Status Updates:</u></p>

			<p>This section displays the status of the job as it pertains to the agreement. If no agreement is accepted, this area remains more-or-less empty.</p>

			<p>If you accept a fixed-price agreement this section will display all of the milestones your freelancer sent you in their agreement. They will display their status and due date.</p>

			<p>If you accept an hourly agreement this section will display the current week and any time your freelancer has tracked. If they use Time Tracker you’ll see screenshots of their work. You’ll also see the current weeks invoice if the system has sent one.</p>

			<p><u>Agreement:</u></p>

			<p>From this tab you can view the current agreement. You can also view the agreement’s status at the top right and view previous agreements if there are any.</p>

			<p><u>SafePay:</u></p>

			<p>From this tab, see the current SafePay balance as well as the SafePay transaction history to keep track of invoice payments, refunds and SafePay funding. You can also request a refund.</p>

			<p><u>Purchase Order Number:</u></p>

			<p>This is an optional feature you can utilize if it helps with your accounting.</p>
