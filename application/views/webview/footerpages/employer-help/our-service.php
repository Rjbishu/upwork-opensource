<p>If you opt into the free 1099 tax form service, Guru.com will issue 1099 tax forms every January on your behalf to the IRS and to qualifying gurus who you have paid more than $600 during the previous tax year.</p>

<p>Guru.com collects W-9 forms from U.S. based gurus so you will not have to worry about verifying the tax IDs of the gurus you hire. And, you will receive a payment report that identifies the 1099 tax forms issued on your behalf.</p>

<p><u>You qualify for the free 1099 tax service if:</u></p>

<p>
	<ul class="help-list">
		<li>You reside in the U.S.</li>
		<li>You paid a guru’s invoice.</li>
		<li>You paid a U.S. guru more than $600 in a single tax year.</li>
		<li>The 1099 status is displayed on each of your guru’s invoices.</li>
	</ul>
</p>

<p><u>1099 Service Applied:    Yes</u></p>

<p>You chose to participate in the free 1099 tax form service for this invoice. If appropriate, Guru.com will issue a 1099 tax form for this guru on your behalf.</p>

<p><u>1099 Service Applied:    No</u></p>

<p>You did not choose to participate in the free 1099 tax form service for this invoice. Guru.com will not issue a 1099 tax form for this guru on your behalf.</p>

<p><u>1099 Service Applied:    Does Not Apply</u></p>

<p>The guru lives outside of the United States. Therefore, the IRS does not require a 1099 form to be issued to the guru.</p>

<p><u>Canceling 1099 Service:</u></p>

<p>You can opt in or out at any time by modifying your account information.</p>

<p>
	<ol class="help-list">
		<li>On the top right of the page, click your name and click “Edit Account”.</li>
		<li>At the bottom of the page, uncheck the “Opt into Guru.com’s FREE 1099 tax service” checkbox.</li>
		<li>Click “Save.”</li>
	</ul>
</p>
