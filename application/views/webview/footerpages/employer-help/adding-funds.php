				<h3><?=$title?></h3>
					<p>SafePay is a system that lets your freelancer know that there are funds available for payment without releasing it until you've received work. SafePay is built into the workroom for every freelancer you hire at no extra cost.</p>

					<p>Funds remain in SafePay indefinitely.  We pay your freelancer only after you approve the completed work and pay their invoice.</p>

					<p><u>The SafePay process</u></p>

					<p>
						<ol class="help-list">
							<li>You hire one or more freelancers.</li>
							<li>Choose to operate with or with an agreement.</li>
							<li>Add enough SafePay funds to meet the minimum as dictated by your effective agreement or add the agreed upon amount directly to SafePay.</li>
							<li>Your freelancer starts and completes the work and sends you an invoice.</li>
							<li>If you approve their work, pay the invoice using the SafePay funds you added earlier.</li>
							<li>You and your freelancer leave feedback for each other.</li>
							<li>Pay an invoice ONLY if you are satisfied with your freelancer's work. Immediately upon paying an invoice, funds are transferred to their cash account and can't be refunded to you.</li>
						</ol>
					</p>

					<p>If you aren't satisfied with their work, don't pay the invoice. Instead, ask for a refund of the SafePay funds. You can then use negotiation and arbitration to resolve disputes.</p>

					<p><u>Adding SafePay Funds</u></p>

					<p>After you hire a freelancer, add SafePay funds using your cash account balance, credit card, PayPal or e-check. You can also send us a check or wired funds. Use the SafePay funds to pay your freelancers' invoices.

					<p><u>To add SafePay funds:</u></p>

					<p>
						<ol class="help-list">
							<li>Click your job title in the "Work Rooms" section of your dashboard.</li>
							<li>Click the SafePay icon underneath your freelancer's screen name.</li>
							<li>Click "Add Funds".</li>
							<li>Select an amount (min. $25.00) and then click "Add Funds". The system will usually know how much you should add, especially if there is an agreement in place.</li>
							<li>Choose a payment method and follow the instructions to complete the process.</li>
							<li>Paying with SafePay</li>
						</ol>
					</p>

					<p>In order to pay your freelancer with funds you added to SafePay, they'll need to send you an invoice first.</p>

					<p><u>To pay an invoice with SafePay funds:</u></p>

					<p>
						<ol class="help-list">
							<li>Click "View and Pay" next to the invoice in the "Invoices" section of your dashboard.</li>
							<li>Click "Pay Invoice".</li>
							<li>Choose whether you're paying the invoice in full or partially.</li>
							<li>On the payment method page, select "SafePay". It will be the first option.</li>
							<li>Follow the rest of the steps to complete your payment.</li>
						</ol>
					</p>

