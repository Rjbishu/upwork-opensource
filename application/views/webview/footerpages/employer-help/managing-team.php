<p><u>Inviting a new user to join your team:</u></p>

<p>You can invite people to your team that do not yet have an account with us.</p>
<p>
	<ol class="help-list">
		<li>Navigate to the dashboard.</li>
    	<li>Click the “Manage” tab.</li>
    	<li>Click “My Managers”.</li>
    	<li>Click “Add”.</li>
    	<li>Enter the email addresses and select a role.</li>
    </ol>
</p>

<p><u>Inviting an existing team member to your job’s team:</u></p>
<p>
	<ol class="help-list">
		<li>Navigate to the job in question.</li>
	    <li>Click “Add a Manager” from the “My Managers” section on the right.</li>
	    <li>Click “Build My Team”.</li>
	    <li>Click “Add from my team”.</li>
	    <li>Select the person you’d like to add.</li>
	    <li>Click “Add Selected”.</li>
    </ol>
</p>


<p><u>Resending a team invitation:</u></p>
<p>
	<ol class="help-list">
		<li>Navigate to the dashboard.</li>
	    <li>Click the “Manage” tab.</li>
	    <li>Click “My Managers”.</li>
	    <li>Hover over the pending team member.</li>
	    <li>Click “Options”.</li>
	    <li>Choose “Resend Invite”.</li>
    </ol>
</p>


<p><u>Team member roles:</u></p>

<table class="table table-bordered">
	<tr>
		<td>Owner</td>
		<td>You are the owner of your freelancer account. Only you can update account settings, including transfer methods and email preferences.</td>
	</tr>
	<tr>
		<td>Administrator</td>
		<td>Administrators can help you run your business and keep your projects on track. They can post, award and add themselves to projects, accept agreements, use Work Room tools, and pay freelancers.</td>
	</tr>
	<tr>
		<td>Manager</td>
		<td>Managers can be added to the team by the Owner or Administrator. Managers can do everything an Administrator can do except pay freelancers, from posting projects to assigning Coordinators and more.</td>
	</tr>
	<tr>
		<td>Coordinator</td>
		<td>Coordinators can access Work Rooms for the projects to which they have been assigned. They cannot post projects, award projects or accept project agreements.</td>
	</tr>
</table>

<p><u>Changing a team member’s role:</u></p>
<p>
	<ol class="help-list">
		<li>Click “Manage” from your dashboard.</li>
	    <li>Click “My Managers”</li>
	    <li>Hover over the team member you want to modify.</li>
	    <li>Click “Options”.</li>
	    <li>Choose “Edit Team Member”.</li>
    </ol>
</p>


<p><u>Removing a team member:</u></p>
<p>
	<ol class="help-list">
		<li>Click “Manage ” from your dashboard.</li>
	    <li>Click “My Managers”</li>
	    <li>Hover over the team member you want to modify.</li>
	    <li>Click “Options”.</li>
	    <li>Choose “Remove from team”.</li>
    </ol>
</p>



