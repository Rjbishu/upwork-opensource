			<h3><?=$title?></h3>
			<p><u>Posting a job:</u></p>
			<p>You can add as much detail to your job posting as you like, but it should include the following:</p>
			<ul class="help-list">
			    <li style="">An outline of the desired task or service.</li>
			    <li>A general description of the business requirements of the job.</li>
			    <li>A timeline detailing expected start and completion dates.</li>
		    </ul>
		    <p><u>You’ll also have to choose the follow preferences when posting a job:</u></p>

		    <ul class="help-list">
			    <li><b>Job Category:</b> You’ll want to pick the category that best fits your job.  If you’re not sure or want to keep it open choose "Any Category".</li>
			    <li><b>Location Preferences:</b> This tells freelancers you want to work with people from a specific area.  You can choose a country, up to three cities, or a US Zip Code.</li>
			    <li><b>Estimated Cost:</b> How much you want to spend on this job.  If you’re not sure there’s a “Not Sure/Confidential” option.</li>
			    <li><b>Hourly/Fixed Price:</b>  Decide if you want to pay someone by the hour or at one set cost.  This can be changed at any point until you approve an agreement.</li>
			    <li><b>Job Exposure:</b>  You can decide who can view the job posting.  This cannot be edited after the job is posted.  There are three options:</li>
		    </ul>
		    <ul class="help-list">
			    <li><u>Maximum:</u> It’s indexed by search engines and there’s no restriction on who can see this.</li>
			    <li><u>Limited:</u> Only Winjobs.com users can see this post.</li>
			    <li><u>None:</u>  Can only be seen by users you invite to the job.  If you don’t invite anyone within 24 hours the job will be made public to our users.</li>
		    </ul>

		    <ul class="help-list">
		    	<li><b>Posting Timeline:</b> Chose when you want to stop receiving quote.  The default is 30 days, but you can set any timeline you like.</li>
		    	<li><b>Attaching Files:</b> You can attached up to 10MB to your job posting.  We support the following: .ACCDB, .AVI, .BMP, .DOC, .DOCX, .EPS, .GIF, .JPEG, .JPG, .MDE, .MOV, .MP3, .MP4, .MPE, .MPEG , .MPG, .PDF, .PNG, .PPS, .PPSX, .PPT, .PPTX, .PSD, .QXD, .SWF, .TIF, .TIFF, .TXT, .WAV, .WMV, .XLS, .XLSX, .ZIP.</li>
		    </ul>
