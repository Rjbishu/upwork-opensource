				<h3><?=$title?></h3>
<p>Employers and freelancers can rate each other within 60 days of payment.</p>

<p>Feedback consists of star ratings (1 to 5) and written comments that are publicly displayed in the Employer Statistics section of the job details and on freelancer profiles. Public feedback allows employers and freelancers to judge each other’s’ reputations prior to hiring or submitting quotes.</p>

<p><u>Leaving feedback:</u></p>

<p>
	<ol class="help-list">
		<li>On the top navigation bar, click “Pay” and click the tab labeled “:Feedback”.</li>
		<li>Find the appropriate record and click “Leave Feedback”.</li>
		<li>Leave a star rating and any comments you want.</li>
	</ol>
</p>

<p><u>NOTE:</u> You have 60 days from the date the Freelancer is paid to leave feedback. Feedback ratings and comments cannot be modified once submitted.</p>

<p><u>Viewing feedback:</u></p>

<p>Feedback you receive from a freelancer will be pending for seven days or until you publish it, whichever comes first. Once published, the feedback will be displayed in the Employer Statistics section of each job you post.</p>

<p>You do have the option of blocking negative feedback. Whether you can block the feedback depends on your blocking power.</p>

<p><u>To view feedback you’ve received:</u></p>

<p>
	<ol class="help-list">
		<li>On the top navigation bar, click “Pay” and click the tab labeled “Feedback.”</li>
<li>Find the appropriate record and click “View Feedback Received”.</li>
	</ol>
</p>

<p>You will be notified by email when you receive feedback. You will then have seven days to either publish or block the feedback. If you do not publish or block it, we will automatically publish it for you after seven days.</p>
<p><u>Understanding pending feedback:</u></p>

<p>The feedback you receive from a freelancer is not immediately displayed for others to see. It will be in a “Pending” status for seven days before being automatically published. During this seven-day period, you can publish the feedback early or you may be able to block it depending on your blocking power. This review period gives you the opportunity to block negative feedback before it is automatically displayed. Once it is published, it will be displayed within the Employer Statistics section of each job you post.</p>

<p><u>Understanding locked feedback:</u></p>

<p>You have 60 days from the time of your payment to leave feedback for the freelancer. After the 60-day period, the record is marked as “Locked (Expired),” and you can no longer give feedback.</p>

<p><u>Publishing feedback:</u></p>

<p>The feedback you receive from a freelancer is not immediately displayed for others to see. It will be in a “Pending” status for seven days before being automatically published. During this seven-day period, you can publish the feedback early or you may be able to block it depending on your blocking power. This review period gives you the opportunity to block negative feedback before it is automatically displayed. Once it is published, it will be displayed within the Employer Statistics section of each job you post.</p>

<p><u>To publish feedback:</u></p>

<p>
	<ol class="help-list">
		<li>On the top navigation bar, click “Pay” and click the tab labeled “Feedback”.</li>
		<li>Find the appropriate record and click “View Feedback Received”.</li>
		<li>Click the “Publish Feedback Now” button in the lower right corner of the feedback record.</li>
	</ol>
</p>

<p><u>Blocking feedback:</u></p>

<p>Once feedback is blocked it cannot be unblocked. Whether you can block feedback depends on your blocking power.</p>

<p><u>To block feedback:</u></p>

<p>
	<ol class="help-list">
		<li>On the top navigation bar, click “Pay” and click the tab labeled “Feedback”.</li>
<li>Find the appropriate record and click “View Feedback Received”.</li>
<li>Click the “Block Feedback” button in the lower right corner of the feedback record.</li>
	</ol>
</p>
<p><u>Calculating blocking power:</u></p>


<p>The number of feedback records you can block depends on the dollar amount of the associated invoices and your current blocking power. Blocking Power may be “spent” on any combination of feedback records as long as the value of the associated invoices does not exceed your blocking power.</p>

<p>Find your blocking power displayed at the bottom of the left-hand column of the “Feedback” page.</p>

<p>Blocking power is 10% of whatever invoices you’ve paid in the last 12 months, minus whatever blocking power you’ve used already.</p>

<p><u>Archiving feedback:</u></p>

You can archive a feedback record on your “Feedback” page no matter the status — pending, published or blocked — of the record. Archiving is simply an organizational function and does not affect the public display of feedback.

<p><u>To archive feedback:</u></p>

<p>
	<ol class="help-list">
		<li>On the top navigation bar, click “Pay” and click the tab labeled “Feedback”.</li>
<li>Find the appropriate record and select “Archive”.</li>
<li>Policies concerning updating or deleting feedback:</li>
	</ol>
</p>

<p>You cannot update feedback left or received, and we will not censor feedback or investigate it for accuracy. If you have sufficient blocking power, however, you can block feedback from being published.</p>

<p><u>Winjobs.com will remove feedback records for the following situations:</u></p>

<p>
	<ul class="help-list">
		<li>If a valid court order finds that the feedback record is libelous, defamatory, or otherwise illegal.</li>
		<li>If you submit a dispute, and during negotiation both parties agree to remove the feedback left for each other.</li>
	</ul>
</p>
<p><u>Winjobs.com will update feedback comments for the following situations:</u></p>

<p>
	<ul class="help-list">
		<li>IPersonal contact information appears in the feedback comments, and a party wants the information to be removed.</li>
		<li>A typo appears in the feedback comments, and a party wants to correct it.</li>
		<li>Profane or obscene language appears in the feedback comments. (Negative language that may not be considered profane is still discouraged and will be reviewed on a case-by-case basis.)</li>
	</ul>
</p>
