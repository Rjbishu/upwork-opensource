				<p><u>E-check (U.S. bank accounts only)</u></p>

				<p>Your e-check account must be verified before using it to pay invoices, add SafePay funds or transfer funds into and out of your cash account. You earn 3.5% Cash back every time you make a payment or add funds using e-check.</p>

				<p><u>To add your e-check account:</u></p>

				<p>
					<ol class="help-list">
						<li>Click “Payment Methods” at the top of your dashboard.</li>
						<li>Click the blue “Add Payment Method” button.</li>
						<li>Choose “Add Bank Account” from the list.</li>
						<li>Fill in the required bank account information.</li>
						<li>Review the information you entered, our Terms of Service and Authorization Confirmation.</li>
						<li>Click “Authorize E-Check Account” and then “Ok”.</li>
						<li>After you submit your account information, we’ll deposit two small, random amounts of $0.25 or less to your bank account for validation. Within 3 business days, check with your bank to obtain these two amounts. Keep in mind, we can’t help you find the deposits. You must access your bank account to view them. If the validation deposits have not appeared in your bank account after 3 business days, contact us so we can resend the deposits.</li>
					</ol>
				</p>

				<p><u>To verify your e-check account:</u></p>

				<p>
					<ol class="help-list">
						<li>Find the two account validation deposits in your bank account.</li>
						<li>Return to the Winjobs.com.</li>
						<li>Click “Payment Methods” at the top of your dashboard.</li>
						<li>Find your bank account from your list of payment methods.</li>
						<li>Enter the two deposit amounts and click the blue “Verify” button. Be sure to enter the amounts as .XX. For example, five cents would be entered as “.05″.</li>
						<li>If you enter the amounts incorrectly 3 times, your e-check account will be locked. Contact us for assistance. You can use your verified e-check account to pay invoices, add Safepay funds, and add funds to your cash account for later use. NOTE: If you have scheduled a transfer from your cash account using e-check, and you delete the e-check account, your transfer will be canceled. Please reschedule your transfer using a different payment method.</li>
