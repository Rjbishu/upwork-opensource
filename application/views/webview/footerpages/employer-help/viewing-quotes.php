<p><u>Understanding the anatomy of a quote:</u></p>
			<ul class="help-list">
	            <li>The freelancer's name and profile icon are displayed at the top right of their quote.</li>
	            <li>The estimated cost will tell you what they expect the job to cost. They can also add a "placeholder" if they are unsure.</li>
	            <li>You can rate each quote for easier sorting using the "My Rating" tool.</li>
	            <li>Click the "Reply" button to send a particular freelancer a private message.</li>
	            <li>The keywords listed underneath the freelancer's name will tell you if key words from their profile match your job.</li>
	            <li>Make sure to click the down arrow button to expand a quote. Only the first line of each quote is displayed by default.</li>
	            <li>Click the freelancer's name to see their profile.</li>
	            <li>Click the blue "Hire" button to hire a freelancer.</li>
	            <li>You can hire more than one freelancer.</li>
	            <li>Click "Delete" to permanently delete the quote from your list.</li>
	            <li>Premium quotes are listed at the top and have a blue diamond icon. Premium quotes cost freelancers extra bids and mean they think they're a great fit for the job.</li>
            </ul>
