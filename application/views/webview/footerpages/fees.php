    <h3 style="font-weight: bold;">For Employers</h3>

    <h4 style="font-weight: bold;">Projects</h4>
    Freelancer is free to sign up, post a project, receive bids from freelancers, review the freelancer's portfolio and discuss the project requirements. If you choose to award the project, and the freelancer accepts, we charge you a small project fee relative to the value of the selected bid, as an introduction fee.

    The cost and how this fee is charged depends on the type of project.

    For fixed price projects, a fee of 3% or $3.00 USD (whichever is greater) is levied at the time a project that has been awarded by you has been accepted by each freelancer you award. If you subsequently pay the freelancer more than the original bid amount we will also charge the project fee on any overage payments.

    For hourly projects, a fee of 3% is levied on each payment as it is made to the freelancer.

    You may cancel the project from your dashboard at any time for up to seven (7) days after the project has been accepted for a full refund of your fee.

    Optional project upgrades may be purchased.

