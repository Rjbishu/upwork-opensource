			<p><u>Checking your job matches via the dashboard:</u></p>

			<p>
				<ol class="help-list">
					<li>Log into your freelancer account.
					<li>Find the “Find Jobs” tile.</li>
					<li>Look for the “Job Matches” button underneath the “Find Jobs” tile.</li>
					<li>If you’ve been invited to one or more jobs you’ll see an orange number next to the “Job Matches” button.</li>
					<li>Click on the “Job Matches” button to actually view your “Job Matches” page.</li>
				</ol>
			</p>

			<p>You will receive the matches if your service and work collection skills match the skills listed by the employer in the job posting and you’ve not yet viewed the job posting online.</p>

			<p>You may see a “Top Match” icon next to your name on a job match. That means your list of services matches that particular job posting really well.</p>

			<p>Job matches are accessible for 30 days but you can delete a job match at any time. Check the checkbox next to one or more job matches and click “Remove” at the top of the screen to delete one or more job matches.</p>

			<p><u>Unsubscribing from job match emails:</u></p>

			<p>
				<ol class="help-list">
					<li>Log into your freelancer account.</li>
					<li>Click “Edit Account” from the top of your dashboard.</li>
					<li>Click the “Email Settings” section on the left sidebar.</li>
					<li>Uncheck “Job Matches”.</li>
					<li>Click “Save”.</li>
