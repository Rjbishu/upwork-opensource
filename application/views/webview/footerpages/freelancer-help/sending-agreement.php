			<h3><?=$title?></h3>
<p>The agreement you send tells our system how to bill your employer. It also sets the scope and establishes timelines. It’s the key to beginning a job successfully.</p>

<p><u>Submitting an agreement:</u></p>

			<p>
				<ol class="help-list">
					<li>Navigate to a job you’ve been hired for.</li>
					<li>Click the “Agreement” tab on the left sidebar.</li>
					<li>Review the options for the agreement.</li>
					<li>Click the blue “Preview” button.</li>
					<li>Review the agreement one last time and click “Send to Employer”</li>
				</ol>
			</p>

<p><u>Three Agreement Types</u></p>

<p><b>“Fixed-Priced Agreement (completed milestones)”</b> You set up milestones. Each Milestone has a value and a due date.  You invoice for a milestone once it’s finished.</p>

<p><b>“Hourly Agreement (weekly time tracked)”</b> You set up the max hours per week, hourly rate and a billing end date.  The system sends  the employer an invoice every Monday based on the time you have tracked the prior week.</p>

<p><b>“Recurring Payments (cyclical billing)”</b> You set a price, billing cycle, and an end date.  You have the option to choose a weekly, biweekly, monthly or quarterly billing.  Invoices for weekly and biweekly will be generated on Monday.   Monthly and Quarterly agreements will be billed on the 1st of each month.</p>

<p><u>Choosing Auto-Payment Terms:</u></p>

<p><b>“Automatic SafePay Funding”</b> will require the employer to automatically fund the next milestone or the next week of work as defined in your agreement. Turning this option on will bill your employer automatically.</p>

<p><b>“Automatic Invoice Payments”</b> sets the timing for when an invoice is paid. If an invoice is sent by you and there are enough funds in SafePay then the system will automatically pay the invoice in 7, 14, or 28 days—it depends on what you select when you set up the agreement. The employer can still pay the invoice sooner if they choose to.</p>

<p><u>Establishing Status Updates:</u></p>

<p>Based on your selection, the system will remind you to update your employer if you haven’t done so within the status update timeline you’ve chosen. You can choose to provide status updates daily, every other day, weekly, every other week, or monthly.</p>

<p>
	<ol class="help-list">
		<li>Find the “I will provide Status Updates” section of the agreement.</li>
		<li>Choose an option from the dropdown.</li>
	</ol>
</p>

<p><u>Establishing the “Scope of Work”:</u></p>

<p>The scope of work can be added here. This should explain in as much detail as you’d like the requirements of this job. You can click the “Attach Files” button to attach images and documents.</p>

<p><u>Editing an agreement:</u></p>

<p>
	<ol class="help-list">
		<li>Navigate to the job in question.</li>
		<li>Click the “Agreement” tab on the left sidebar.</li>
		<li>Scroll to the bottom of the page.</li>
		<li>Click the gray “Edit” button.</li>
		<li>Make changes to the agreement.</li>
		<li>Click the blue “Preview” button.</li>
		<li>Review the agreement one last time and click “Send to Employer”.</li>
	</ol>
</p>
