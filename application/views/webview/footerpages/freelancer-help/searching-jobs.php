			<h3><?=$title?></h3>
			<p>Searching for jobs is entirely based off of key words and filters.</p>

			<p>Each key word you type will narrow your job search. For example, “php and wordpress developer” will find you jobs pertaining to PHP and WordPress development.<p>

			<p>You can also apply various filters to narrow your results further.<p>

			<p><u>To search the site for jobs:</u></p>

			<p>
				<ol class="help-list">
			    	<li>Log into your freelance account.</li>
					<li>Find the “search jobs” search bar at the top of the screen.</li>
					<li>Search using whatever key words you’d like. (ie. “wordpress”)</li>
					<li>Review your result as-is, OR,</li>
					<li>Refine your search using the sidebar of filters on your left.</li>
				</ol>
			</p>

			<p>It’s best to at least choose a category on the left sidebar but you can forego that and search the entire site if you’d like.</p>

			<p><u>Tips for searching:</u></p>

			<p>
				<ul class="help-list">
			    	<li>Check your spelling.</li>
					<li>Narrow your search using filters.</li>
					<li>Use quotations to do exact searches. For example, searching for “php development” with quotations will only display results with that exact phrase somewhere in the posting.</li>
					<li>Use a hyphen to exclude a specific keyword. For example, searching for “php –development” will only display results that contain “php” but do NOT contain “development”.</li>
				</ul>
			</p>

			<p><u>Saving your search:</u></p>

			<p>
				<ol class="help-list">
			    	<li>Define your search criteria using keywords, categories, budget, location and other settings options in the sidebar on the left.</li>
					<li>Click the “Save Search” button at the top right or in the left sidebar.</li>
					<li>Choose a name for that saved search.</li>
					<li>Click “Save Search”.</li>
					<li>Your saved searched will appear on your dashboard within the “Find Jobs” tile.</li>
				</ol>
			</p>

			<p><u>You can also subscribe to an RSS feed:</u></p>

			<p>
				<ol class="help-list">
			    	<li>Define your search criteria using keywords, categories, budget, location and other settings options in the sidebar on the left.</li>
					<li>Click the “RSS” icon at the top right of the screen.</li>
				</ol>
			</p>

			<p>Follow the on-screen instructions that show up with your browser.</p>
