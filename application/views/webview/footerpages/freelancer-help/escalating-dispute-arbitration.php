			<p><u>To escalate to arbitration:</u></p>

			<p>
				<ul class="help-list">
					<li>Click “View Dispute” next to the appropriate invoice in the “Invoices” section of your dashboard.</li>
					<li>Click “Escalate to Arbitration”.</li>
				</ul>
			</p>

			<p>The respondent will have two business days to provide any addition evidence or comments before the case will be sent to our arbitrators.</p>

			<p><u>Evidence to provide:</u></p>

			<p>
				<ul class="help-list">
					<li>Any additional agreements both parties accepted.</li>
					<li>Files or instructions from the employer.</li>
					<li>Screenshots of any discussion that pertains to your case.</li>
					<li>Completed work with notes about why it met the employer’s requirements.</li>
				</ul>
			</p>

			<p><b>NOTE:</b> Only screenshots will be accepted of communication.  Text pasted into a text document will not be accepted.</p>

			<p>Once a ruling has been reached it will be posted to Winjobs.com and the funds will be disseminated according to the ruling provided by the arbitrators.  You will receive an email with the distribution of funds and a link to view the ruling.</p>
			
