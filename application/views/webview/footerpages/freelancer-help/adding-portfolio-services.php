			<p>Your Portfolio showcases work you’ve completed.</p>

			<p><u>Adding or editing a work collection within your portfolio:</u></p>

			<p>
				<ol class="help-list">
					<li>Log into your account.
					<li>Click your name on your dashboard to view your profile.</li>
					<li>Click “Edit Profile” at the top left.</li>
					<li>Click the “Portfolio” tab from the left sidebar.</li>
					<li>Click the “+” button at the top right to add a work collection.</li>
					<li>Click directly on an existing work collection to edit it.</li>
				</ol>
			</p>
			<p>When adding content to a work collection you’ll be presented with three options.</p>

			<p>
				<ul class="help-list">
					<li><b>File:</b> You’ll have the option to browse your computer and upload the following: .JPEG, .GIF, .TIFF,.DOC, .DOCX, .RTF, .PDF, .PPS, .PPSX, .PPT, .PPTX, .TXT, .XLS, .XLSX, .PS, .ODT, .SXW, .ODP, .SXI, .ODS, .SXC.</li>
					<li><b>Text:</b> A text box that you can type or paste text into. This is great for a paragraph or two of text.
					<li><b>Embed Media:</b>  If you have audio or video on another site, you can use the embed link to have it show in your profile. Make sure to grab the embed HTML from your video sharing site and paste it into this section.</li>
				</ul>
			</p>

			<p>Organize your work into collections that are to your liking.  You can edit these at any time.</p>

			<p>Services will show employers what kind of work you have experience in.</p>

			<p><u>Adding a service to your profile:</u></p>

			<p>
				<ol class="help-list">
					<li>Log into your account.</li>
					<li>Click your name on your dashboard to view your profile.</li>
					<li>Click “Edit Profile” at the top left.</li>
					<li>Click the “Services” tab from the left sidebar.</li>
					<li>Click the “+” button at the top right to add a service.</li>
					<li>Click directly on an existing service to edit it.</li>
					<li>Understanding the anatomy of a service:</li>
				</ol>
			</p>

			<p>
				<ul class="help-list">
					<li><b>Title</b> -the title that will be displayed on your profile.</li>
					<li><b>Description</b> - a short description of the service you’re offering.</li>
					<li><b>Category</b> -the category your service falls into.</li>
				</ul>
			</p>

			<p><u>NOTE:</u> It’s important to choose categories that fit the jobs you want to acquire.  This will determine if you are alerted to new jobs posted with the same category.</p>

