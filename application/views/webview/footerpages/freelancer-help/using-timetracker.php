			<h3><?=$title?></h3>
			<p>Our time tracker software automatically tracks your time as you work. You can use it in conjunction with your agreement to automatically have the system bill your employer at the end of the week. Time tracker is optional.</p>

			<p><u>Downloading time tracker:</u></p>

			<p>
				<ol class="help-list">
					<li>Make sure you’ve been hired for a job already.</li>
					<li>Navigate to the job in question (it can be any job).</li>
					<li>Click the “Time” tab.</li>
					<li>Click “Download Time Tracker Now” on the right sidebar.</li>
				</ol>
			</p>

			<p>Once you run time tracker, it will begin taking random screenshots of your desktop within ten-minute intervals. Within five minutes of capture, the screenshots are uploaded to our site for review by you and your employer.</p>

			<p>You can manually add time or edit time that time tracker has tracked from the “Time” tab as well.</p>

			<p><u>Running time tracker on your computer:</u></p>

			<p>Depending on your operating system, these instructions may vary a little.</p>

			<p>
				<ol class="help-list">
					<li>For Windows, from your “All Programs” menu, click “freelancer Time Tracker”.</li>
					<li>For Linux, from your “bin” directly, execute the “run.sh” command.</li>
					<li>For Mac, from your “Applications” folder, click “freelancer Time Tracker”.</li>
				</ol>
			</p>

			<p><u>Viewing screenshots:</u></p>

			<p>
				<ol class="help-list">
					<li>Navigate to the job in question.</li>
					<li>Click the “Time” tab.</li>
					<li>Select the date.</li>
					<li>Click the thumbnail.</li>
				</ol>
			</p>

			<p>To view more information regarding your activities in the given 10-minute interval, click the task title listed under the thumbnail.</p>

			<p>The employer will see only the screenshots captured by time tracker, and the accompanying notes that you add. If time tracker captures a screenshot that you do not want to share with the employer, you have the option of deleting it before or after it is uploaded.</p>

			<p><u>Adding a note to a screenshot:</u></p>

			<p>
				<ol class="help-list">
					<li>Navigate to the job in question.</li>
					<li>Click the “Time” tab.</li>
					<li>Select the date.</li>
					<li>Click the thumbnail.</li>
					<li>Click “Modify”</li>
					<li>Add a note and click “Modify”.</li>
				</ol>
			</p>
			<p><u>Deleting a screenshot before it is uploaded:</u></p>

			<p>You can delete screenshots before or after they are uploaded.</p>

			<p>To delete a screenshot before it is uploaded, click the “Discard” option in Time Tracker when the pop-up warns you of the impending upload.</p>

			<p><u>Deleting a screenshot after it is uploaded:</u></p>

			<p>
				<ol class="help-list">
					<li>Navigate to the job in question.</li>
					<li>Click the “Time” tab.</li>
					<li>Select the date.</li>
					<li>Select the check box to the left of the screenshot you want to delete.</li>
					<li>Click the “Delete” button.</li>
				</ol>
			</p>


			<p><u>Changing the job you’re tracking time for:</u></p>

			<p>
				<ol class="help-list">
					<li>Open the time tracker menu from your computer.</li>
					<li>Select the “Job: <Current Job>” dropdown.</li>
					<li>Select a new job from the list.</li>
				</ol>
			</p>
			<p><u>Changing the task you’re tracking time for:</u></p>

			<p>
				<ol class="help-list">
					<li>Open the time tracker menu from your computer.</li>
					<li>Select the “Update Task” option.</li>
					<li>Select a new task.</li>
				</ol>
			</p>

			<p><u>Changing the preferences within time tracker:</u></p>

			<p>
				<ol class="help-list">
					<li>Open the time tracker menu from your computer.</li>
					<li>Click “Preferences”.</li>
				</ol>
			</p>

			<p><u>Manually enter time for a job:</u></p>

			<p>If you do not wish to track time automatically you can still add time manually.</p>

			<p>
				<ol class="help-list">
					<li>Navigate to the job in question.</li>
					<li>Click the “Time” tab.</li>
					<li>Click “Add Time”.</li>
					<li>Click and drag across the cells on the screen. Each cell represents 10 minutes.</li>
					<li>Enter any notes you’d like.</li>
					<li>Click “Add Time”.</li>
				</ol>
			</p>
