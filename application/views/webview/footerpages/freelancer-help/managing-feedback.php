			<h3><?=$title?></h3>
			<p><u>Leaving feedback:</u></p>

			<p>Within 60 days of being paid, you have the opportunity to leave feedback for the employer. Feedback consists of ratings and written comments that are publicly displayed on freelancer profiles and in the Employer Statistics section of each job. Public feedback allows freelancers and employers to judge each other’s’ reputations on Winjobs.com prior to submitting proposals or hiring.</p>

			<p>The feedback window closes 60 days after you are paid. Be sure to leave ratings and comments before the window closes.</p>

			<p>Once you have left feedback, it cannot be modified. Additionally, we do not assist with disputes over feedback ratings and comments.</p>

			<p><u>To leave feedback:</u></p>

			<p>
				<ol class="help-list">
					<li>On the top navigation bar, click “Payments” and click the tab labeled “Feedback”./li>
					<li>Find the appropriate record and click “Leave Feedback”.</li>
					<li>Leave a star rating and any comments you want.</li>
				</ol>
			</p>

			<p><u>NOTE:</u> The feedback you leave an employer will not immediately display within the employer’s Winjobs.com account. Like you, employers have the option of blocking or publishing the feedback they receive.</p>

			<p><u>Viewing feedback:</u></p>

			<p>You will receive an email as soon as an employer leaves feedback for you. While you can always view feedback via the “Feedback” page, the feedback you receive will not be immediately published and displayed within your profile. You have seven days from the time the feedback is left to review and either publish or block the feedback.</p>

			<p><u>To view feedback you’ve received:</u></p>

			<p>
				<ol class="help-list">
					<li>On the top navigation bar, click “Payments” and click the tab labeled “Feedback”.</li>
					<li>Find the appropriate record and click “View Feedback Received”.</li>
					<li>Understanding pending feedback:</li>
				</ol>
			</p>

			<p>The feedback you receive from an employer is not immediately displayed within your profile. It will be in a “Pending” status for seven days before being automatically published. During this seven-day period, you can publish the feedback early or you may be able to block it depending on your blocking power. This review period gives you the opportunity to block negative feedback before it is automatically displayed.</p>

			<p><u>Understanding locked feedback:</u></p>

			<p>You have 60 days from the time of your payment to leave feedback for the employer. After the 60-day period, the record is marked as “Locked (Expired),” and you can no longer give feedback.</p>

			<p><u>Publishing feedback:</u></p>

			<p>
				<ol class="help-list">
					<li>On the top navigation bar, click “Payments” and click the tab labeled “Feedback”.</li>
					<li>Find the appropriate record and click “View Feedback Received”.</li>
					<li>Click the “Publish Feedback Now” button in the lower right corner of the feedback record.</li>
				</ol>
			</p>
			<p><u>Blocking feedback:</u></p>

			<p>Once feedback is blocked it cannot be unblocked. Whether you can block feedback depends on your blocking power.</p>

			<p><u>To block feedback:</u></p>

			<p>
				<ol class="help-list">
					<li>On the top navigation bar, click “Payments” and click the tab labeled “Feedback”.</li>
					<li>Find the appropriate record and click “View Feedback Received”.</li>
					<li>Click the “Block Feedback” button in the lower right corner of the feedback record.</li>
				</ol>
			<p><u>Calculating blocking power:</u></p>

			<p>The number of feedback records you can block depends on the dollar amount of the associated invoices and your current blocking power. Blocking Power may be “spent” on any combination of feedback records as long as the value of the associated invoices does not exceed your blocking power.</p>

			<p>Find your blocking power displayed at the bottom of the left-hand column of the “Feedback” page.</p>

			<p>Blocking power is 10% of whatever invoices you’ve been paid in the last 12 months, minus whatever blocking power you’ve used already.</p>

			<p><u>Archiving feedback:</u></p>

			<p>You can archive a feedback record on your “Feedback” page no matter the status — pending, published or blocked — of the record. Archiving is simply an organizational function and does not affect the public display of feedback.</p>

			<p><u>To archive feedback:</u></p>

			<p>
				<ol class="help-list">
					<li>On the top navigation bar, click “Payments” and click the tab labeled “Feedback”.</li>
					<li>Find the appropriate record and select “Archive”.</li>
				</ol>
			</p>

			<p><u>Policies concerning updating or deleting feedback:</u></p>

			<p>You cannot update feedback left or received, and we will not censor feedback or investigate it for accuracy. If you have sufficient blocking power, however, you can block feedback from being published.</p>

			<p><u>Winjobs.com will remove feedback records for the following situations:</u></p>

			<p>
				<ul class="help-list">
					<li>If a valid court order finds that the feedback record is libelous, defamatory, or otherwise illegal.</li>
					<li>If you submit a dispute, and during negotiation both parties agree to remove the feedback left for each other.</li>
				</ul>
			</p>

			<p><u>Winjobs.com will update feedback comments for the following situations:</u></p>

			<p>
				<ul class="help-list">
					<li>Personal contact information appears in the feedback comments, and a party wants the information to be removed.</li>
					<li>A typo appears in the feedback comments, and a party wants to correct it.</li>
					<li>Profane or obscene language appears in the feedback comments. (Negative language that may not be considered profane is still discouraged and will be reviewed on a case-by-case basis.)</li>
				</ul>
			</p>

