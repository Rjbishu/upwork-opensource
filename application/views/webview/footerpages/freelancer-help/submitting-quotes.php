			<p><u>Using bids</u> Bids are the currency used to apply for jobs. You spend your bids each time you apply for a job. Freelancers receive a limited number of bids with their membership. Subscribing members receive 50 bids per month (600 if you purchased an annual membership) and basic (free) members receive 10 bids per month. If you have used up all of your allotted bids you can still apply for jobs to which you are invited or you can buy more bids. Your membership bids will replenish at the beginning of each billing cycle. For Professional, Business, and Executive members, any unused bids from the previous billing cycle will rollover to the next. They’ll be limited to 100, 200 or 300 rollover bids a year, respectively. Your remaining bid count is listed at the top right corner of the Find Jobs section on your dashboard. You can also buy more bids and view your bidding statistics here.</p>

