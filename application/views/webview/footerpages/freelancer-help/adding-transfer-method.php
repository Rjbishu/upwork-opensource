			<p><b>Wire Transfer (Non-U.S. Bank Accounts Only)</b></p>

			<p>Wire transfer is only available to freelancers with non-U.S. bank accounts. There is a $9.00 wire transfer fee for every wire transfer sent.</p>

			<p>To transfer funds from your Winjobs.com cash account to your non-U.S. bank account via wire transfer, you must first register your bank account information on Winjobs.com.</p>

			<p>There is nothing you need to do to confirm your wire transfer account. We will upload your information to our bank to confirm that they have all the information they need to send funds to your bank. Once our bank gives us the thumbs up, we will mark your wire transfer account as confirmed.</p>

			<p>Confirmation should occur within one business day (Monday through Friday) of registration.</p>

