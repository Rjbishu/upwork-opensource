			<p><u>Tracking expenses:</u></p>

			<p>Every time you receive a payment, purchase or renew a membership, buy extra bids, or buy a skill test you receive confirmation online and via email. Additionally, yearly expense reports are available in January. The expense report includes all Transaction Fees paid as well as all purchases made during the selected calendar year. You will see an “Expense Reports” link on the Dashboard page if you have any yearly reports available.</p>

			<p><u>W9 Requirement:</u></p>

			<p>The W-9 form is used by U.S. based Freelancers to report their tax identification numbers to their Employers for tax purposes.</p>

			<p>The 1099-Miscellaneous Income Form (1099-MISC) is the tax form sent to U.S. based independent contractors who have earned more than $600 the previous year.  It is the freelance equivalent of the W-2 form, which is sent to employees. To generate a 1099 form, independent contractors must complete and submit a W-9 tax form to the issuing agency.</p>

			<p>1099 forms contain the following information:</p>

			<p>
				<ol class="help-list">
					<li>Freelancer’s name and address</li>
					<li>Freelancer’s social security or tax ID number</li>
					<li>Payer’s name, address and telephone number</li>
					<li>Payer’s federal ID number (EIN)</li>
					<li>Amount of non-employee compensation</li>
				</ol>
			</p>

			<p>To receive a 1099 for the previous year you must:</p>

			<p>
				<ol class="help-list">
					<li>Reside in the U.S.</li>
					<li>Have received payments through Winjobs.com totaling $600 or more</li>
					<li>Have been paid by an employer who participated in our 1099 service</li>
					<li>Have completed a W-9/1099 on file using a SSN</li>
				</ol>
